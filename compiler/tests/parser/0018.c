int main() {
    int a = 10;
    int b = 5;

    // '&' (address-of) unary operator
    // int* ptr = &a;

    // '*' (dereference) unary operator
    // int dereferenced = *ptr;

    // '+' (positive) unary operator
    int positive = +a;

    // '-' (negation) unary operator
    int negation = -b;

    // '~' (bitwise NOT) unary operator
    int bitwise_not = ~a;

    // '!' (logical NOT) unary operator
    int logical_not = !a;
    return 0;
}
